    /**
     * 获取电话记录时间
     *
     * @param millisecond
     * @return
     */
    public static String getPhoneRecord(String millisecond) {
        Long mills = Long.valueOf(millisecond);
        Long curr = System.currentTimeMillis();

        if (curr - mills < 120000) return "刚刚";
        String millsDate = format(mills, Y_M_D);
        String currentDate = format(curr, Y_M_D);
        if (millsDate.equals(currentDate)) {
            int hour = Integer.valueOf(format(mills, HH));

            if (hour >= 0 && hour < 6) {
                return "凌晨" + format(mills, H_M);
            } else if (hour >= 6 && hour < 12) {
                return "上午" + format(mills, H_M);
            } else if (hour == 12) {
                return "中午" + format(mills, H_M);
            } else if (hour >= 13 && hour < 18) {
                return "下午" + format(mills, H_M);
            } else if (hour == 18) {
                return "傍晚" + format(mills, H_M);
            } else {
                return "晚上" + format(mills, H_M);
            }
        } else {
            if (curr - mills <= 24 * 60 * 60 * 1000) {
                return "昨天" + format(mills, H_M);
            } else if (curr - mills <= 7 * 24 * 60 * 60 * 1000) {
                return format(mills, EEE) + format(mills, H_M);
            } else if (curr - mills <= 365 * 24 * 60 * 60 * 1000) {
                return format(mills, M_D) + format(mills, H_M);
            } else {
                return format(mills, "yyyy年MM月dd日") + format(mills, H_M);
            }
        }
    }

